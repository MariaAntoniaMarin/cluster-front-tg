<h1>Acerca de este sitio</h1>

<p>Aplicación web para el agrupamiento de datos de georreferenciación, mediante el uso del algoritmo 
	k-Means y criterios de validez del agrupamiento resultante.</p><br>

<h3>Modulos desarrollados para esta pagina web</h3>

<ul>
	<li>Módulo de software para el preprocesamiento de los datos georreferenciados.</li>
	<li>Módulo de software para encontrar el número óptimo de grupos, mediante la aplicación de
		distintos criterios de validez.</li>
	<li>Módulo de software que realice el agrupamiento k-Means de datos georreferenciados, a partir de un
		número indicado, o un número óptimo de grupos a buscar.</li>
</ul>

